function soma(a, b) {
    return a + b
}
function subtracao(a, b) {
    return a - b
}
function divisao(a, b) {
    return a / b
}
function mult(a, b) {
    return a * b
}
console.log('a soma dos numeros é' + soma(3, 3))
console.log('a subtração dos numeros é' + subtracao(3, 3))
console.log('a divisão dos numeros é' + divisao(3, 3))
console.log('a multiplicação dos numeros é' + mult(3, 3))


